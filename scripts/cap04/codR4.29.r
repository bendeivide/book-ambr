# Operador '>' entre vetores de comprimento 1
1 > 3

# Operador '<' com vetor de comprimento maior que 1
1 < c(0, 1, 3)

# Operador '==' entre vetores
c(1, 2, 3) == c(3, 2, 1)

# Operador '%in%' verificando se os elementos do primeiro vetor estao no segundo vetor
1 %in% c(3, 4, 5)

# Operador '%in%' verificando se os elementos do primeiro vetor estao no segundo vetor (Reciclagem de vetor)
c(1, 2) %in% c(3, 4, 5)

# Operador '%in%' verificando se os elementos do primeiro vetor estao no segundo vetor
c(1, 2, 3) %in% c(3, 4, 5)

# Operador '%in%' verificando se os elementos do primeiro vetor estao no segundo vetor
c(1, 2, 3, 4) %in% c(3, 4, 5)